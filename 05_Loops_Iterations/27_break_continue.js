// break, continue in JavaScript 🚀🔥

// break ✔️
for (let i = 1; i <= 20; i++) {
  if (i == 5) {
    console.log("Detected 5");
    break;
  }
  console.log(`Value of i is : ${i}`);
}

// continue ✔️
for (let i = 1; i <= 20; i++) {
  if (i == 5) {
    console.log("Detected 5");
    continue;
  }
  console.log(`Value of i is : ${i}`);
}
